package com.tmt.kontroll.content.persistence.selections;

/**
 * Enum representing boolean operators to match given values to their references. 
 * 
 * @author Serg Derbst
 *
 */
public enum ReferenceOperator {

	IsEqual,
	IsNotEqual,
	IsSmallerAs,
	IsLargerAs,
	IsSmallerOrEqualAs,
	IsLargerOrEqualAs;
}
